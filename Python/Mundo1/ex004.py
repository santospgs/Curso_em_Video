
print(f'====== DESAFIO 4 ======')
var = input('Insira um valor: ')

print(type(var))
print(f'É alfabético? {var.isalpha()}')
print(f'É decimal? {var.isdecimal()}')
print(f'É numérico? {var.isnumeric()}')
print(f'É maiúsculo? {var.isupper()}')
print(f'Está capitalizado? {var.istitle()}')
print(f'É um digito? {var.isdigit()}')
print(f'É espaço? {var.isspace()}')